package it.fpili.happy_health_clinic.enums;

/**
 * Enumeration of user roles in the system.
 */
public enum Role {
    /**
     * Patient role for users seeking medical services.
     */
    PATIENT,

    /**
     * Doctor role for medical professionals providing services.
     */
    DOCTOR,

    /**
     * Administrator role for system management and configuration.
     */
    ADMIN
}