package it.fpili.happy_health_clinic.enums;

/**
 * Enumeration of possible employment statuses.
 */
public enum EmploymentStatus {
    /**
     * Employee is actively employed and working.
     */
    ACTIVE,

    /**
     * Employee is temporarily on leave.
     */
    ON_LEAVE,

    /**
     * Employee's employment has been terminated.
     */
    TERMINATED
}